        }
      },
      Outputs :> {
        {
					OutputName -> "vantHoffPlot",
					Description -> "Plot of the van't Hoff fit generated by thermodynamic analysis.",
					Pattern :> ValidGraphicsP[]
				}
      }
    },

		{
			Definition -> {"PlotThermodynamics[thermoObjects]", "vantHoffPlots"},
			Description -> "generates van't Hoff plots using the points and fit generated by thermodynamic analyses in 'thermoObjects'.",
			Inputs :> {
				{
					InputName -> "thermoObjects",
					Description -> "The analysis objects which is the output of smoothing analysis performed with AnalyzeThermodynamics.",
					Widget -> Adder[
						Widget[Type->Object,Pattern:>ObjectP[Object[Analysis,Thermodynamics]],PatternTooltip->"A single analysis object."]
					]
				}
			},
			Outputs :> {
				{
					OutputName -> "vantHoffPlots",
					Description -> "Plots of the van't Hoff fit generated by thermodynamic analyses.",
					Pattern :> ValidGraphicsP[]
				}
			}
		}

	},

