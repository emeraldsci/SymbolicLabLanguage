	BasicDefinitions -> {

    {
      Definition -> {"PlotThermodynamics[thermoObject]", "vantHoffPlot"},
      Description -> "generates a van't Hoff plot using the points and fit generated by thermodynamics analysis in 'thermoObject'.",
