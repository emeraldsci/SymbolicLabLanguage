			Class -> Expression,
			Pattern :> RefractiveIndexReadingModeP,
			Description -> "Refractive index reading mode.",
			Category -> "General"
		},
		Calibrant -> {
			Format -> Single,
			Class -> Link,
			Pattern :> _Link|Null,
			Relation -> Alternatives[Object[Sample],Model[Sample]],
			Description -> "Calibrant used for calibration measurement.",
			Category -> "General"
		},
		CalibrationTemperature -> {
			Format -> Single,
			Class -> Real,
			Pattern :> GreaterP[0 Celsius]| Null,
			Description :> "Temperature in which refractive index of calibrant is measured.",
			Category -> "General"
		},
