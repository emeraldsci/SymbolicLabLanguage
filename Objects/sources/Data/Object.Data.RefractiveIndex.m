			Format -> Multiple,
			Class -> Expression,
			Pattern :> {{GreaterEqualP[0 Second], GreaterP[1]}..}|Null,
			Description -> "The collection of measured refractive index of a sample in kinetic experiment.",
			Category -> "Experimental Results"
		},
		RefractiveIndexTemperatureProfile -> {
			Format -> Multiple,
			Class -> Expression,
			Pattern :> {{GreaterP[0 Celsius], GreaterP[1]}..}|Null,
			Description -> "The measured refractive index of a sample in temperature scan experiment.",
			Category -> "Experimental Results"
		},
		DataFiles -> {
			Format -> Multiple,
			Class -> Link,
			Pattern :> _Link,
			Relation -> Object[EmeraldCloudFile],
			Description -> "The file containing the raw unprocessed data generated by the instrument.",
			Category -> "Experimental Results"
		}
	}
