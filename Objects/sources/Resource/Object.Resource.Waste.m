DefineObjectType[Object[Resource, Waste], {
	Description->"A set of parameters describing the waste generated by the requesting protocol.",
	CreatePrivileges->None,
	Cache->Session,
	Fields -> {

		WasteType -> {
			Format -> Single,
			Class -> Expression,
			Pattern :> WasteTypeP,
			Description -> "The category fo disposal for this resource.",
			Category -> "Resources"
		},
		Sample -> {
			Format -> Single,
			Class -> Link,
			Pattern :> _Link,
			Relation -> Alternatives[
				Object[Sample],
				Object[Container],
				Object[Part],
				Object[Plumbing],
				Object[Item],
				Object[Sensor],
				Object[Wiring]
			],
			Description -> "The sample used to fulfill this resource.",
			Category -> "Resources"
		},
		WasteDescription -> {
			Format -> Single,
			Class -> String,
			Pattern :> _String,
			Description -> "A brief summary of the waste generated.",
			Category -> "Product Specifications"
		},
		Amount -> {
			Format -> Single,
			Class -> VariableUnit,
			Pattern :> GreaterEqualP[0*Milliliter] | GreaterEqualP[0*Milligram] | GreaterEqualP[0*Unit, 1*Unit],
			Description -> "The amount of waste that was generated by the given protocol.",
			Category -> "Resources"
		}
	}
}];
